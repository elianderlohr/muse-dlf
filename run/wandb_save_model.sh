#!/bin/bash

# Activate the virtual environment
echo "Activating virtual environment..."
source run/venv/bin/activate

# Environment Setup
echo "Setting up environment..."
# Verify Python version
echo "Python version:"
python --version
echo "pip version:"
python -m pip --version

# Display installed package versions for verification
echo "Installed package versions:"
python -m pip list

# Load WANDB_API_KEY from .env file
echo "Loading WANDB_API_KEY from .env file..."
if [ -f ".env" ]; then
    export $(grep -v '^#' .env | xargs)
else
    echo "Error: .env file not found!"
    exit 1
fi

# Check if WANDB_API_KEY is loaded
if [ -z "$WANDB_API_KEY" ]; then
    echo "Error: WANDB_API_KEY is not set!"
    exit 1
else
    echo "WANDB_API_KEY successfully loaded."
fi

# Parameters
PROJECT_NAME=$1
RUN_NAME=$2

# Construct the directory path
DIR_PATH="models/${PROJECT_NAME}/${RUN_NAME}"

# Run the Python script
python src/helper/wandb_save_model.py --project "$PROJECT_NAME" --dir_path "$DIR_PATH" --artifact_name "$RUN_NAME" --artifact_type "model"
